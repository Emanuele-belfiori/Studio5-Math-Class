public class Main {
    public static void main(String[] args) {
        System.out.println("Math Class: ");

        /*
         *  Math class:
         *
         *  - alcuni metodi: abs, min, max, pow, sqrt
         *
         *  - attributi: PI
         */

        System.out.println(Math.abs(-55)); // Numero assoluto toglierà il segno

        System.out.println(Math.max(55,21)); // Troverà il massimo valore

        System.out.println(Math.min(55,21)); // Troverà il minimo valore

        System.out.println(Math.pow(5, 3)); // Potenza= calcolerà 5 alla 3°

        System.out.println(Math.sqrt(25)); // Calcola la radice quadrata di 25

        System.out.println(Math.PI); // Calcola il P Grco (¶)
    }
}